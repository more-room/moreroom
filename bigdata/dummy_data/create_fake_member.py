# DB 연결 및 데이터 삽입
def insert_user_data_to_db(user_data):
    try:
        load_dotenv()
        # MySQL 연결 설정
        connection = mysql.connector.connect(
            host = os.getenv('HOST'),
            user = os.getenv('USER'),  # 데이터베이스 사용자 이름
            password = os.getenv('PASSWORD'),  # 데이터베이스 비밀번호
            database = os.getenv('DATABASE'),  # 사용할 데이터베이스 이름
            port = os.getenv('PORT')
        )
        cursor = connection.cursor()

        # INSERT 쿼리
        insert_query = """
        INSERT INTO Member (email, password, nickname, gender, regionId, birth, clearRoom, createdAt, delFlag, photo)
        VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s)
        """
        
        # 데이터를 하나씩 삽입
        for user in user_data:
            cursor.execute(insert_query, (
                user['email'], user['password'], user['nickname'], user['gender'], 
                user['region_id'], user['birth'], user['clear_room'], 
                user['created_at'], user['del_flag'], user['photo_url']
            ))
        
        # 변경사항 커밋
        connection.commit()
        print(f"{len(user_data)}개의 사용자 데이터가 성공적으로 삽입되었습니다.")

    except mysql.connector.Error as error:
        print(f"데이터 삽입 중 오류 발생: {error}")

    finally:
        # 연결 종료
        if connection.is_connected():
            cursor.close()
            connection.close()
            print("MySQL 연결이 종료되었습니다.")

